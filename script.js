(function initPremiumScrollExperience(){
  const prefersReducedMotion = window.matchMedia('(prefers-reduced-motion: reduce)').matches;

  function createScrollProgress(){
    if(document.querySelector('.scroll-progress')) return;
    const bar = document.createElement('div');
    bar.className = 'scroll-progress';
    document.body.appendChild(bar);
  }

  function updateScrollProgress(){
    const bar = document.querySelector('.scroll-progress');
    if(!bar) return;
    const doc = document.documentElement;
    const maxScroll = doc.scrollHeight - window.innerHeight;
    const progress = maxScroll > 0 ? (window.scrollY / maxScroll) * 100 : 0;
    bar.style.width = `${Math.min(100, Math.max(0, progress))}%`;
  }

  function updateNavState(){
    const nav = document.querySelector('nav');
    if(!nav) return;
    nav.classList.toggle('nav-scrolled', window.scrollY > 40);
  }

  function prepareRevealElements(){
    const revealGroups = [
      ...document.querySelectorAll('.reveal-up'),
      ...document.querySelectorAll('#benefits .luxury-card'),
      ...document.querySelectorAll('#ingredients .group'),
      ...document.querySelectorAll('#ingredients .luxury-card'),
      ...document.querySelectorAll('#formula .luxury-card'),
      ...document.querySelectorAll('#formula .formula-card'),
      ...document.querySelectorAll('#results .relative.group'),
      ...document.querySelectorAll('#results .border-l-2'),
      ...document.querySelectorAll('#product .product-slider'),
      ...document.querySelectorAll('#product .space-y-6 > *'),
      ...document.querySelectorAll('#faq details'),
      ...document.querySelectorAll('footer .grid > div')
    ];

    const uniqueElements = [...new Set(revealGroups)];

    uniqueElements.forEach((el, index) => {
      el.classList.add('premium-reveal');

      if(index % 7 === 1) el.classList.add('reveal-left');
      if(index % 7 === 3) el.classList.add('reveal-right');

      const localIndex = [...(el.parentElement?.children || [])].indexOf(el);
      const delay = Math.min(Math.max(localIndex, 0) * 85, 420);
      el.style.setProperty('--reveal-delay', `${delay}ms`);
    });
  }

  function observeRevealElements(){
    const elements = document.querySelectorAll('.premium-reveal, .reveal-up');

    if(prefersReducedMotion){
      elements.forEach(el => el.classList.add('active'));
      return;
    }

    const premiumObserver = new IntersectionObserver((entries) => {
      entries.forEach(entry => {
        if(entry.isIntersecting){
          entry.target.classList.add('active');
          premiumObserver.unobserve(entry.target);
        }
      });
    }, {
      threshold: 0.16,
      rootMargin: '0px 0px -8% 0px'
    });

    elements.forEach(el => premiumObserver.observe(el));
  }

  function animateStats(){
    const statElements = [...document.querySelectorAll('#results .text-4xl, #results .text-5xl')]
      .filter(el => /\d/.test(el.textContent));

    if(prefersReducedMotion || !statElements.length) return;

    const statObserver = new IntersectionObserver((entries) => {
      entries.forEach(entry => {
        if(!entry.isIntersecting) return;

        const el = entry.target;
        if(el.dataset.animated === 'true') return;
        el.dataset.animated = 'true';

        el.dataset.originalText = el.textContent;
        const target = parseInt(el.textContent.replace(/\D/g, ''), 10);
        const duration = 1400;
        const start = performance.now();

        function tick(now){
          const progress = Math.min((now - start) / duration, 1);
          const eased = 1 - Math.pow(1 - progress, 3);
          const suffix = /%/.test(el.dataset.originalText || el.textContent) ? '%' : '';
          el.textContent = `${Math.round(target * eased)}${suffix}`;

          if(progress < 1){
            requestAnimationFrame(tick);
          } else {
            el.textContent = `${target}${suffix}`;
          }
        }

        requestAnimationFrame(tick);
        statObserver.unobserve(el);
      });
    }, { threshold: 0.55 });

    statElements.forEach(el => statObserver.observe(el));
  }

  function initCtaGlow(){
    const cta = document.querySelector('section.bg-primary');
    if(!cta || prefersReducedMotion) return;

    cta.addEventListener('mousemove', (event) => {
      const rect = cta.getBoundingClientRect();
      const x = ((event.clientX - rect.left) / rect.width - 0.5) * 40;
      const y = ((event.clientY - rect.top) / rect.height - 0.5) * 40;
      cta.style.setProperty('--cta-x', `${x}px`);
      cta.style.setProperty('--cta-y', `${y}px`);
    });
  }

  function initPremiumExperience(){
    createScrollProgress();
    prepareRevealElements();
    observeRevealElements();
    animateStats();
    initCtaGlow();
    updateScrollProgress();
    updateNavState();

    window.addEventListener('scroll', () => {
      updateScrollProgress();
      updateNavState();
    }, { passive: true });
  }

  if(document.readyState === 'loading'){
    document.addEventListener('DOMContentLoaded', initPremiumExperience);
  } else {
    initPremiumExperience();
  }
})();
