// Image Slider Logic

let currentSlide = 0;
let autoSlide = null;
const slideDelay = 5000;

function getSlides(){
    return document.querySelectorAll(".slider-image");
}

function getDots(){
    return document.querySelectorAll(".dot");
}

function showSlide(index){

    const slides = getSlides();
    const dots = getDots();

    if(!slides.length) return;

    if(index >= slides.length) index = 0;
    if(index < 0) index = slides.length - 1;

    slides.forEach((slide, slideIndex) => {
        slide.classList.toggle("active", slideIndex === index);
    });

    dots.forEach((dot, dotIndex) => {
        dot.classList.toggle("active", dotIndex === index);
    });

    currentSlide = index;
}

function nextSlide(){
    showSlide(currentSlide + 1);
    restartAutoSlide();
}

function prevSlide(){
    showSlide(currentSlide - 1);
    restartAutoSlide();
}

function goToSlide(index){
    showSlide(index);
    restartAutoSlide();
}

function startAutoSlide(){
    stopAutoSlide();
    autoSlide = setInterval(() => {
        showSlide(currentSlide + 1);
    }, slideDelay);
}

function stopAutoSlide(){
    if(autoSlide){
        clearInterval(autoSlide);
        autoSlide = null;
    }
}

function restartAutoSlide(){
    startAutoSlide();
}

function openProductModal(){
    const slides = getSlides();
    const modal = document.getElementById("product-modal");
    const modalImg = document.getElementById("product-modal-image");

    if(!slides.length || !modal || !modalImg) return;

    modalImg.src = slides[currentSlide].src;
    modal.classList.remove("hidden");
    modal.setAttribute("aria-hidden", "false");
    document.body.style.overflow = "hidden";
    stopAutoSlide();
}

function closeProductModal(){
    const modal = document.getElementById("product-modal");

    if(!modal) return;

    modal.classList.add("hidden");
    modal.setAttribute("aria-hidden", "true");
    document.body.style.overflow = "auto";
    startAutoSlide();
}

window.addEventListener("load", () => {
    const slider = document.querySelector(".product-slider");
    const imageWrapper = document.querySelector(".product-image-wrapper");
    const modal = document.getElementById("product-modal");
    const closeModalBtn = document.getElementById("product-modal-close");

    showSlide(0);
    startAutoSlide();

    if(slider){
        slider.addEventListener("mouseenter", stopAutoSlide);
        slider.addEventListener("mouseleave", startAutoSlide);
    }

    if(imageWrapper){
        imageWrapper.addEventListener("click", (event) => {
            if(event.target.closest("button")) return;
            openProductModal();
        });
    }

    if(closeModalBtn){
        closeModalBtn.addEventListener("click", closeProductModal);
    }

    if(modal){
        modal.addEventListener("click", (event) => {
            if(event.target === modal){
                closeProductModal();
            }
        });
    }

    document.addEventListener("keydown", (event) => {
        if(event.key === "Escape"){
            closeProductModal();
        }
    });
});

